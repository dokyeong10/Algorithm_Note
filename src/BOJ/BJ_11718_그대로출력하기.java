package BOJ;

import java.util.Scanner;

public class BJ_11718_그대로출력하기 {
    public static void main(String[] args) {
        Scanner sc =new Scanner(System.in);
        while(sc.hasNext()){
            String s = sc.nextLine();

            System.out.println(s);
        }
    } // end of main
} // end of class
